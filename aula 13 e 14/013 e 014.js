var m = 1
while (m <= 10) { // while se lê enquanto
    console.log (`Testando loops: ${m}`)
    m ++ // m = m + 1
}

var m = 1
do {  // do serve para repetição com teste logico no final
    console.log (`Testando loops: ${m}`)
    m ++ // m = m + 1  
} while (m <= 10)


for (var m=1; m<=10; m++) { // for tem 3 elementos : 1- inicio, 2- teste logico, 3- incremento
    console.log (`Testando loops: ${m}`)
}




